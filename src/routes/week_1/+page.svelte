<h4 id="reading_currents">Reading current</h4>
<p>
  The fastest current is usually deepest.  That doesnt mean it is a clean path per se. 
</p>

<ul>
  <li>The fastest current is usually deepest.
    <ul>
      <li>That dosen't mean it is a clean path per se... usually though.   </li>
    </ul>
  </li>
  <li>River Bends
    <ul>
    <li>The outside of a bend is usually fastest / deepest.</li>
  <li>The outside of a bend may want to push you into the outside bank.</li>
  <li>The inside of a bend is shallower / slower.</li>
  <li>Cheating the bend just to the inside will help pull you around the bend.  The slow water will want to pull you in.  Riding that edge can be helpfull.</li></ul>
  </li>
</ul>

<p>As the river drops, more rocks will appear. All rivers strive to consolidate a channel. even at low water.  This is when you  get to know every rock in a river, and where the low water channel is.</p>

<p>Alot of runs in Colorado have definate low water channels by July (Arkansas, Lower Eagle), that may not be apparent.  It can be helpfull to follow other boats (probes).  but not too close...</p>
<p>Other boats may not know the line...  Your group may have a "Bob Chadwick", who will see you following his line and lead you right into a rock, while he spins around it last second and laugh at you.  Friggin Bob...  Beware the Chadiwicks.  Don't follow too close.</p>


<p>
  Some rapids start from a slow wide channel over a gradient change, or into a narrower channel.  Some may seem uniform and calm across the entrance.  There may be a  prevalent 'bubble line' entering this rapid.  You may know bubble lines from fishing.  They form, disperse, then come together when currents converge.  This is a good clue at the top of a rapid. Put this in the back of your mind and notice bubble lines.  If you are in slim to no current... look for bubbles.
</p>


