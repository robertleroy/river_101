  <h4 id="week_1">Day 1: Upper Colorado </h4>
  <p>
    Day one:  An exercise of bouncing off one river bank to the other, then back.  Most people have a really hard time just staying in slow moving current on day one.  <em class="sidenote">I assume lack of awareness of various current changes ~ or ~ the awareness of the boats direction/momentum and how to stop/counter boat momentum/residual drift.</em> I remember this day from my training trip also.  Awkward first steps...  
  </p>
  <p>
    I learned on oars.  Most trips were oar rigs in 1987 or 88 I think it was.  That changed completely in the next couple of years.  I think it is easier to learn all the river stuff on oars as opposed to learning to manage a crew at the same time.  On oars all you need to do is learn which hand is right, which left.  And you don't have to tell anyone about it.
  </p>
  <p>
    Either way, day one is mostly getting a feel for how heavy the boat is, how quick or how sluggish it is to spin.  First experiences with the force of the current, managing and countering momentum, balancing that with the speed and direction of the current.  How well or how poorly the boat tracks.
  </p>
  <p>
    The first time at the wheel, I would task people to spin both directions, fast, slow.  Forward and Back: With, Against, and Across the current.  Just to feel it.  A good game to play is to get the boat spinning, then pick a direction. "3 o'clock" or "7 o'clock", being the orientation to the river to stop the rotation and hold the angle.  Just getting a feel for the boat, develop new muscles and memory.  Paddle boats need to learn how much you can do with a paddle vs how much you can do with a crew.  You get 2 10ft paddles!  Bonus.  The spinning game is way easier with oars.  Being aware of and holding angles <strike>should</strike> <ins>will</ins> become second nature.
  </p>

  <p>This is your first goal.  Pick a stretch with slow current and few features.  Try to stay in the current.  Get a feel for the weight and momentum of the boat. Once you can stay in a clear wide open channel, time to move on.</p>

  

  <h4>Day Two: Upper Colorado</h4>
  <p>
    Random goals / games on the way downstream.  Throw a paddle in the river and chasing it down.  Pick random spots (eddies) on the river and make your move to get there.  Endless suggestions of "point way above your target".  "Start your move way early".  It dosen't matter how many times you say it, it takes about 3 days to begin to hear it apparently.  So this is a universal learning hurdle.  Realizing that you need to take into account the 3 mph you are moving downstream when you plot an angle or route, and that the boats momentum may decide to continue it's direction until told not to..  It just takes some time to get that feel.   
  </p>
  <p>This is really goal 2.  Not necesarily catching an eddy, but practicing the general angles and momentum to acheive the goal. Anticipating.  Planning ahead and making the move early.</p>
  <p>
    Hint:  If your goal is across the river and 50 yds downstream, and you pick your angles and route to get to a spot over there but 20 yds up...  then you your final move will be a small one.  You can break it into little peices.  Get 90% of the move done before you get there, if that makes sense.  A little energy way early, is worth way more than trying to do all the work later.  2 or 3 good pulls earlly, then let that small momentum continue across, beats rowing like an SOB last minute.
  </p>
  <p>
    Another day two scenario: <br>
     "See that Big Rock (BFR) in the river, the only one around?" <br> "Don't hit it."  
     <br>
    That pretty much assures they are going to hit it,   
    <br>  
    because now they are focued on it.
    <br>
    I would see this in the vast majority of people in the first few days.  
    <br>
    You can see it on their faces, and tell that they will hit it 30 yds before they do.  
  </p>
  <p>
    Don't focus on the obsticle.  
    <br>
    Notice the obsticle, then focus on the clear path.  
    <br>
    That is how you drive, how you ride a bike...  
    <br>
    Focus on your lane.   
    <br>
    If you catch yourself obsessively focused on the obsticle,  
    <br>
    take a breath, re-orient your focus.
  </p>

  <h4>Day Three:</h4>
  <p>Eddies.  Big ass eddies.  I can't tell you how many bushes in slow water I have been dragged through on training trips.  These are NOT eddies (lol).  Day 3: <em>Pick big ass eddies</em>.  If you are past the eddy, in slow water, you will continue to go down stream..  Make a plan B. </p>

  <p> Again, don't be so focused on plan A that you fail to realize you have already missed it.  Don't argue with the river about it.</p>
  <p>I really like the strech below State Bridge for this in the spring. (State Bridge to Bond). 
  No rapids here.  A riffle then a float, then a riffle then a float...  Great eddies after each little riffle. 
  A few of them were great for going from the eddy on one side to the eddy on the other.  Quick little 'ferries' back and forth across the riffle. (39.8548, -106.6642).  Late summer these features may not be as defined as I am thinking, but a nice float all the same.  If you can find a narrow riffle with eddies on both sides, you can learn alot ferrying back and forth.  Try it swimming...??  <small><em> With <abbr title="Personal Floatation Device">PFD</abbr> of course. </em></small></p>

  <p>Many people begin to show good progress on day 3.  Still making moves late, but beginning to anticipate.  Setting angles more aggressively, though often not enough.  Better at holding lines and angles.  Beginning to be able to read the currents and obsticles.  Few people having difficulty staying in the current.</p>

  <p>More paddle chasing.  Sets good short term goals.  Spontaneous event driven goals requiring response and adaptation.</p>

  <h4>Day Four: </h4>
  <p>Easy class 3.  Step it up .. half a notch.  Perfecting the art of getting stuck on rocks, getting unstuck. Getting better at seeing little rocks ahead of time and avoiding them. (anticipate)  </p>
  <p>My advise is to keep working on eddies.  Catch an eddy, fish for 5 minutes, go find another.</p>


  <br>

  <h4>Week Two: Lower Eagle</h4>

  <p>Easy class 3 shouldn't be a problem.  Beginning to run larger more complex class 3 </p>

  <ul>
    <li>Larger rapids</li>
    <li>Complex rapids, linking multiple moves</li>
    <li>Scouting rapids</li>
    <ul>
      <li>Identify obsticles / hazards</li>
      <li>Identify possible routes</li>
      <li>Plot out strategies to acheive routes.</li>
      <li>Identify alternatives (plan B?)</li>
    </ul>
    <li>Challenging yourself with more difficult eddies.</li>
    <li>Intro to Rescue
      <ul>
        <li>Swimming</li>
        <li>Throw bag practice</li>
      </ul>
    </li>
  </ul>

  <p>Start to challenge yourself with smaller eddies. pick an eddy thats harder to get to, plan and link moves together...</p>

  <p>The last day of training we would set up an obsicle course for the trainees.  So it meant ferrying back and forth, catch an eddy and touch a life jacket on shore with a paddle, maybe throwing a throw bag through a hula hoop, maybe portaging across an island...  weird odd stuff.  6-8 objectives to hit within maybee 50 or 100 yds of river.  They were already broken into teams through the class, but needed to pick a guide among themselves.  We would walk & talk them through the course, tell them it will be timed, and there will be time penalties if you miss an objective.  The winners get to.. win</p>
  <p>Those people learned more in this hour than the whole 2 weeks.  There was no self consciousness going on, no overthinking or stressing about doing something right or wrong.  Inhibitions dropped and they were just boaters at that point.  With a visceral desire to beat everyone else.  They were focused on the best way to get from point A to point B, and then C, D and E.   And noone was going to tell them how to do it.  They had to be resourcful, efficient and many of the obsticles made them think out of the box.  They were on their own.  There would be some impressive moves, and commical misses, and everyone loved race day.</p>
  
