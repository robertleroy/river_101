  <h3 id="kit">What to carry</h3>
  <dl>
    <dt>Carry a patch kit and a pump with you</dt>
    <dd>
      Consult Downriver Equipment on what glue to use & how to use it.
    </dd>
    <dd>NRS has online repair guides.</dd>
    <dd>
      Boat will be Hypalon or PVC, different glues and material
      respectively.
    </dd>
    <dd class="sub">I recall Star boats are PVC - 'Stabond' glue</dd>
    <dd class="sub">Double check, these things change</dd>
    <dd class="sub">
      <strong>DO NOT store Stabond inside!!!</strong> fumes are wicked...
    </dd>
    <dd class="sub" />

    <br />
    <dt>Pliers, screwdriver, knife - Leatherman is a beautiful thing</dt>

    <dd>Should have a 60ft throw rope.. handy for a million things.</dd>
    <dd class="sub">rescue, drying clothes, the bucket game....</dd>
    <dd class="sub">extending bow line for tying off raft...</dd>
    <dd class="sub">in case of lost or broken straps....</dd>
    <dd class="sub">Oh yeah.. Rescue.</dd>

    <br />
    <dt>I didn't notice if you had a spare oar. Consider it</dt>
    <dd>There are ways to tether your oars to the raft.</dd>
    <dd class="sub">Shaft through a metal ring,</dd>
    <dd class="sub">Webbing from ring tied to raft,</dd>
    <dd class="sub">
