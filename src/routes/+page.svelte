  <h3 id="swimming" style="margin-top: 1rem">Swimming 101:</h3>

  <dl>
    <dt>If you fall out of the boat...</dt>
    <dd>Get back in</dd>
    <dd>Practice it so that you know you can.</dd>
    <dd>Find the best technique for you.</dd>

    <br />

    <dt>For me:</dt>
    <dd>One hand on a handle or D-ring, something solid.</dd>
    <dd>Other hand on top of the tube.</dd>
    <dd class="sub">Lunge/pull/kick/thrust -</dd>
    <dd class="sub">
      Lock the elbow straight of the arm holding the handle.
    </dd>
    <dd class="sub">
      Lock that elbow, then it is easy enough to throw a leg over and scamper
      on in.
    </dd>
  </dl>

  <br />

  <dl>
    <dt>Swimming Strategies</dt>
    <dt>A. Defensive</dt>
    <dd>On your back, feet up, facing downstream.</dd>
    <dd>Get your bearings...</dd>
    <dd>If you are gonna bounce off of a rock,.</dd>
    <dd class="sub">bounce with your feet,</dd>
    <dd class="sub">not your head.</dd>
    <br />
    <dt>B. Aggressive</dt>
    <dd>Mark Spitz it to the boat or shore</dd>
    <dd class="sub">
      <em>
        <a
          style="text-decoration: underline dotted"
          href="https://olympics.com/en/athletes/mark-spitz"
          target="_blank"
          rel="noopener noreferrer">...1972 Olympics reference</a
        > &emsp; ie. swim real fast
      </em>
    </dd>
    <br />
    <dt>In Practice: A & B</dt>
    <dd>Pick your route, just like in a boat.</dd>
    <dd>Defensive when you need to be, aggressive when you can.</dd>
    <dd>Keep in mind swimming in cold whitewater is f~ing exhausting.</dd>
    <dd class="sub">Bide you energy if you need to</dd>
    <dd>I am going to get out as quick as possible</dd>
    <dd class="sub">Unless I am having fun.</dd>
    <br />
    <dd class="sub">
      <em>it was somewhat common for me to see customers gasping when entering
        cold water. For some reason they would just start inhaling... no
        exhaling. Just linked inhales... , so only half of breathing. It
        dosen't work that way. If you find this type of distress... be mindful
        of exhaling. Can have very calming / centering effect</em>.
    </dd>

    <br /><br />

    <dt>DON'T PUT YOUR FEET DOWN IN THE CURRENT</dt>
    <dd>until you're in an eddy</dd>
    <dd>or basically on shore</dd>
    <br />
    <dd>Trying to slow yourself with your feet is just not a thing</dd>
    <dd class="sub">
      unless it is super shallow, like you're already dragging you butt.
    </dd>
    <br />
    <dd>Foot entrapment is a nightmare scenario.</dd>
  </dl>

