  <h3 id="highside">Highside, Wraps</h3>
  <dl>
    <dt>Don't hit things sideways.</dt>
    <dd>Rocks, Big Waves / Holes, Bridges.</dd>

    <br />
    <dt>If you do, dont hit them in the middle</dt>
    <dd>try to spin off if you can.</dd>
    <dd class="sub">or more to one side than the other.</dd>

    <br />
    <dd>Hitting a rock sideways in strong current.</dd>
    <dd class="sub">May push one side up on the rock.</dd>
    <dd class="sub">
      Even worse the upstream current might grab the upstream tube and suck
      it under.
    </dd>
    <dd class="sub">
      Either way, can dumptruck, flip, or wrap a raft <small><em>see below</em></small>.
    </dd>

    <br />
    <dt>'Highside' means to get your weight on the downstream tube</dt>
    <dd>the tube on the rock - ASAP.</dd>
    <dd>
      Get your weight and all passengers on the downstream tube, before it
      starts to flip.
    </dd>

    <br />
    <dd>
      This is an effort / attempt to counter to forces mentioned above.
    </dd>
    <dd class="sub">Weight the downstream tube - harder to push it up.</dd>
    <dd class="sub">
      Un-Weight the upstream tube - harder for the current to grab it. May
      even lift the upstream tube out of the current.
    </dd>
    <dd class="sub"><small><em>see below</em></small>.</dd>

    <br />
    <dt>Flipping almost always flips upstream</dt>
    <dd>Rafting motto, "rig to flip".</dd>
    <dd>I dont think I've ever heard a fisherman say that...</dd>
    <dd class="sub">but what do I know about fishing.</dd>

    <dd class="sub">It was years before my first flip.</dd>

    <br />
    <dt>Wrapping a boat</dt>
    <dd>
      Usually you will bounce off, deflect and or ride the pillow wave /
      wash off of the face of the rock, to one side or the other.
    </dd>
    <dd class="sub">&lbrace; see definition of 'pillow wave' &rbrace;.</dd>
    <br />
    <dd>If you broadside that rock in strong current dead center...</dd>
    <dd class="sub">The current may be equal strength to both sides.</dd>
    <dd class="sub">Can't decide which way to wash you off.</dd>
    <dd class="sub">
      May push you into the rock in a way that displaces that pillow wave
      from the rock onto your boat.
    </dd>

    <br />
    <dd class="sub">That is a wrap.</dd>
    <dd class="sub">Think: pasted on that rock like a postage stamp.</dd>
    <dd class="sub">
      Recovery will be covered in a much later and more advanced version of
      this website.
    </dd>
    <dd class="sub"><small><em>see below</em></small></dd>

