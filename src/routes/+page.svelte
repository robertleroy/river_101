  <h3 id="misc">Inflation:</h3>
  <dl>
    <dt>Hypalon and PVC == really tough material, but:</dt>
    <dd>
      Dont leave tubes topped off on shore / trailer in hot sun for too long.
    </dd>
    <dd class="sub">
      Heat will increase the psi, can blow a baffle that way.
    </dd>
    <dd class="sub">Deflate a bit, top it off after shuttle.</dd>

    <br />
    <dd>Dont store in garage topped off either.</dd>
    <dd class="sub">
      it's just stress that isnt needed on seems/material....
    </dd>
    <dd class="sub">Releive the pressure a little bit.</dd>

    <br />
    <dt>A soft raft on a trailer isn't a great idea either...</dt>
    <dd>You know, that unambiguous not too hard / not too soft thing.</dd>

    <br />
    <dd>keep these concepts in back of your mind.....</dd>
    <dd>You'll get a feel for it quickly.</dd>
  </dl>

