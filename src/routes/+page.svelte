  <h3 id="boating">Boating 101:</h3>
  <dl>
    <dt>Always wear a lifejacket, zipped up:</dt>
    <dd><strong>Colorado State Law</strong></dd>
    <dd>Good idea</dd>
    <dd>Make it a personal value.</dd>
    <dd>Passengers too.</dd>
    <br />
    <dd>
      <em>Even Fishermen? oh hell, I don't know -
        <br />
        Class II rapids I gotta say yes,
        <br />
        watch for Rangers in flatwater..
        <br />
        They can give out tickets, so be nice.</em>
    </dd>
    <br />

    <dd>
      <em>If your habit is 'only when I need too',
        <br />
        will you forget to when you need it?</em>
    </dd>
    <br />

    <dd>Utah has a flat water exception.</dd>
    <br />
  </dl>

  <dl>
    <dt>Rowing</dt>
    <dd>Pulling is way faster / stronger than pushing</dd>
    <dd>if you need to make a big move,</dd>
    <dd class="sub">default to pulling until you get a feel for it.</dd>

    <br />
    <dt>Make your move early. Way early.</dt>
    <dd>Keep in mind your momentum and the velocity of the current.</dd>
    <dd>Aim way upstream of where you want to go.</dd>
    <dd>Kind of like leading your receiver (football, lacrosse?),</dd>
    <dd class="sub">you have to compensate for downstream flow dynamics.</dd>
    <br />
    <dd>One or two strokes way upstream is way better</dd>
    <dd class="sub">than working too hard when your late on your move.</dd>
  </dl>

  <br />

  <dl>
    <dt>Islands</dt>
    <dd>Given two or more channels:</dd>
    <dd>
      When in doubt, choose the one that looks like it has the most water
      going into it.
    </dd>
    <dd>You could get out and have a look also.</dd>
  </dl>

  <br />

  <dl>
    <dt>Eddy's</dt>
    <dd>Cross eddy lines with aggressive angles and speed.</dd>
    <dd>
      The contrary currents (eddy line) will spin the boat into the eddy.
    </dd>
    <dd>Crossing unaggressively may deflect and spin you out.</dd>
    <dd>Same thing goes for leaving the eddy.</dd>
    <dd>Charge in, charge out.</dd>

    <br />
    <dt>
      Really really strong eddies (contrary currents) may suck a tube under
    </dt>
    <dd class="">can be abrupt and suprising.</dd>
    <dd class="">doubtfull you will experience this for a while,</dd>
    <dd class="">
      though spring high water is when you will see that the most.
    </dd>

    <br />
    <!-- <dt>Best way to catch an eddy</dt> -->
    <dt>Enter eddies as high as possible with momentum</dt>
    <dd>The eddy line is most defined there.</dd>
    <dd>The contrary current is strongest there</dd>
    <dd>Enter too low and its really just slow water...</dd>
    <dd class="sub">The eddy line is murky, undefined</dd>
    <dd class="sub">You missed the eddy.</dd>
    <br />
    <dd>Dont miss the take out eddy.</dd>
  </dl>

  <br />

  <dl>
    <dt>Angles</dt>
    <dd>Fastest way to the other side: pulling 90&deg; to current.</dd>
    <dd>Crossing with least downstream travel: 45&deg; to current.</dd>
    <dd class="sub">
      <em>45&deg; ferry angle:</em> pulling across and upstream.
    </dd>
    <dd class="sub">less than 45&deg; may be warrented but...</dd>
    <dd class="sub">
      diminishing efficency and usefulness with smaller angles.
    </dd>
    <br />
    <dd>Smartest way to the other side may be to eddy hop, if available.</dd>
    <dd class="sub">Use the slow water behind an eddy to your advantage.</dd>
  </dl>

  <br />
  
  <dl>
    <dt>Flow</dt>
    <dd>Outside of the bend is faster, deeper, stronger, pushier.</dd>
    <dd class="sub">pushes towards the outer bank.</dd>
    <br />
    <dd>Inside of the bend is slower.</dd>
    <dd class="sub">will gradually spin you and draw you in.</dd>
    <dd class="sub">
      It is much less work to cheat the bend to the inside.
    </dd>
    <dd class="sub">Will be shallower also.</dd>
  </dl>

  <br />

  <dl>
    <dt>
      Dipping an oar in an eddy you're passing will spin your boat quickly
    </dt>
    <dd>So will dipping the bow or stearn in.</dd>
    <dd>can be useful, can be fun.</dd>
    <dd>Play with it a bit, it will teach you.</dd>
    <dd>I think eddys are the best <s>beginner</s> skill to master.</dd>
  </dl>

  <br />

  <dl>
    <dt>If you need to get somewhere.. Reverse engineer it</dt>
    <dd>Look upstream of where you want to go</dd>
    <dd class="sub">
      for the chunk of water that will take you where you want to go.
    </dd>

    <br />
    <dd>
      In other words, you dont need to row the boat between two rocks.
    </dd>
    <dd class="sub">
      You need to put the boat on the upstream chunk of water that will take
      you through.
    </dd>

    <br />
    <dd>Let the river do the work.</dd>
    <dd class="sub">Over simplistic, but the principle is sound.</dd>
    <dd class="sub">Use the currents to your advantage.</dd>
    <br />
    <dd>I've thrown a lot of sticks in the river above rapids</dd>
    <dd class="sub">to see where different currents will take it.</dd>
  </dl>

  <br />

  <dl>
    <dt>Watch for rocks.</dt>
    <dd>As the river goes down there will be more.</dd>

    <br />
    <dt>Watch for little humps of water that you can't see behind.</dt>
    <dd>Those are rocks.</dd>

    <br />
    <dt>Watch for horizon lines and waves that you cant see behind.</dt>
    <dd>That is... well you might want to pull over and find out.</dd>

    <br />
    <dt>Loud crashing water sounds can be a clue to.. something</dt>

    <br />
    <dt>If the river ahead looks kind of like popcorn popping,</dt>
    <dd>That is really shallow water.</dd>
    <dd>
      A place where you <strike>might</strike> <ins>are going to</ins> get stuck.
    </dd>

    <br />
    <dt>Watch for Great Blue Herons.</dt>
    <dd>I love Great Blue Herons.</dd>
  </dl>

