  <h3>The Bucket Game:</h3>

  <dl>
    <dt>Tug of war while standing on 5 gal buckets.</dt>
    <dd>60' throw rope, 2 buckets 40'ish feet apart.</dd>
    <dd class="sub">there should be plenty of slack for both sides.</dd>
    <br />
    <dd>
      Game starts with each player on a bucket - holding one END of the rope.
    </dd>
    <br />
    <dd>On 'GO'</dd>
    <dd class="sub">Pull as much slack to your side as fast as you can.</dd>
    <dd class="sub">Pull opponent off of bucket.</dd>
    <dd class="sub">Give slack when he pulls, knocks him off much easier.</dd>
    <dd class="sub">
      Don't give all your slack, then you are at the end of your rope.
    </dd>
    <dd class="sub">Kind of like fishing: reel him in or let him run....</dd>
    <dd class="sub">Letting go of rope = lose.</dd>
    <br />
    <dd>There is a secret to the game....</dd>
    <dd class="sub">not intuitive to most ppl....</dd>
    <dd class="sub">I can't tell you what it is....</dd>
